export function translate(locale: Locale, key: string, values?: Record<string, string | number>): string {
  const dictionary = translations[locale] ?? translations[DEFAULT_LOCALE];
  const fallback = translations[DEFAULT_LOCALE][key] ?? key;
  const template = dictionary[key] ?? fallback;

  if (!values) {
    return template;
  }

  return Object.entries(values).reduce((result, [name, value]) => {
    return result.replaceAll(`{${name}}`, String(value));
  }, template);
}

export function getLocaleDisplayName(locale: Locale): string {
  return translate(locale, `locale.${locale}`);
}

export function getStatusLabel(status: string, locale: Locale): string {
  return translate(locale, `status.${status}`);
}

export function localizeProgramName(program: string, locale: Locale): string {
  return programLabels[program]?.[locale] ?? program;
}

function prettifyKey(value: string): string {
  return value
    .replace(/[_-]+/g, " ")
    .replace(/\b\w/g, (part) => part.toUpperCase());
}

export function localizeLabel(value: string, locale: Locale): string {
  return signalLabels[value]?.[locale] ?? prettifyKey(value);
}

export function localizeLabels(values: string[], locale: Locale): string[] {
  return values.map((value) => localizeLabel(value, locale));
}

export function localizeSeverity(value: string, locale: Locale): string {
  return severityLabels[value.toLowerCase()]?.[locale] ?? localizeLabel(value, locale);
}

export function formatDate(iso: string, locale: Locale): string {
  return new Date(iso).toLocaleDateString(locale === "ru" ? "ru-RU" : "en-US", {
    day: "2-digit",
    month: "short",
    year: "numeric",
  });
}

export function formatDateTime(iso: string, locale: Locale): string {
  return new Date(iso).toLocaleString(locale === "ru" ? "ru-RU" : "en-US", {
    day: "2-digit",
    month: "short",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}

export function formatPercent(value: number): string {
  return `${(value * 100).toFixed(0)}%`;
}

export function getProgramOptions(locale: Locale): Array<{ value: string; label: string }> {
  return Object.entries(programLabels)
    .filter(([value]) => value !== "General Admissions")
    .map(([value, labels]) => ({ value, label: labels[locale] }));
}

export function getCountryOptions(locale: Locale): Array<{ value: string; label: string }> {
  const formatter = new Intl.DisplayNames([locale === "ru" ? "ru-RU" : "en-US"], {
    type: "region",
  });

  return countryCodes
    .map((code) => ({
      value: code,
      label: formatter.of(code) ?? code,
    }))
    .sort((left, right) => {
      if (left.value === "KZ") return -1;
      if (right.value === "KZ") return 1;
      return left.label.localeCompare(right.label, locale === "ru" ? "ru" : "en");
    });
}
